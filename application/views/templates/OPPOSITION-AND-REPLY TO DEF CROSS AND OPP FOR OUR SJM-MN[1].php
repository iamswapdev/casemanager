<p class=MsoNormal style='text-align:justify;line-height:150%'><b
style='mso-bidi-font-weight:normal'><u><span style='font-size:11.0pt;
line-height:150%'>Opposition to Lack of Medical Necessity<o:p></o:p></span></u></b></p>

<p class=p29 style='margin-left:0in;text-indent:16.15pt;line-height:150%;
tab-stops:0in 16.15pt'><span style='font-size:11.0pt;line-height:150%'>3.<span
style='mso-tab-count:1'>    </span>Plaintiff is entitled to Summary Judgment
because the medical services at issue were medically necessary. In support of
this contention, Plaintiff provides an affirmation from Dr. _________________
(Exhibit “A”). After reviewing the relevant medical records for the Assignor
and the peer review performed by the Defendant’s expert, it is
Dr._______________ professional opinion that the medical services at issue were
<b style='mso-bidi-font-weight:normal'>MEDICALLY NECESSARY</b>. It is also his
opinion that the conclusions made in the Defendant’s peer review are <b
style='mso-bidi-font-weight:normal'>NOT CORRECT.</b><o:p></o:p></span></p>

<p class=p29 style='margin-left:0in;text-indent:16.15pt;line-height:150%;
tab-stops:0in 16.15pt'><span style='font-size:11.0pt;line-height:150%'>4.<span
style='mso-tab-count:1'>    </span>Therefore, Plaintiff is entitled to Summary
Judgment. In the alternative, should this Honorable Court determine that the
Plaintiff is not entitled to Summary Judgment, this evidence creates a genuine
issue of fact and this Court should deny the Defendant’s Cross-Motion for
Summary Judgment. <o:p></o:p></span></p>

<p class=p29 style='margin-left:0in;text-indent:16.15pt;line-height:150%;
tab-stops:0in 16.15pt'><b style='mso-bidi-font-weight:normal'><u><span
style='font-size:11.0pt;line-height:150%'>The Defendant’s Proof Fails to Prima
Facie Demonstrate a Lack of Medical Necessity for the Treatment in Question<o:p></o:p></span></u></b></p>

<p class=p29 style='margin-left:0in;text-indent:16.15pt;line-height:150%;
tab-stops:0in 16.15pt'><span style='font-size:11.0pt;line-height:150%'>5.<span
style='mso-tab-count:1'>    </span>In the case at bar, the defendant relies
upon expert medical opinion to allege that the services at issue were not
medically necessary. However, the defendant’s proof fails to prima facie
demonstrate a lack of medical necessity for the treatment in question.<o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>6.<span
style='mso-tab-count:1'>   </span>To find treatment or services are not
medically necessary, it must be reasonably shown by medical evidence, in
consideration of the patient’s condition, circumstances, and best interest of
the patient, that the <b style='mso-bidi-font-weight:normal'>treatment or
services would be ineffective or that the insurer’s preferred health care
treatment or lack of treatment would lead to an equally good outcome.</b> <u>Acupuncture
Prima Care, P.C. v. State Farm <span class=SpellE>Mut</span>. Auto Ins. Co.</u>,
12830/07 (Civ. Ct. Nassau Co. 2007) (Emphasis Added).<o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>7.<span
style='mso-tab-count:1'>   </span>To meet its burden, at a minimum, the <b
style='mso-bidi-font-weight:normal'>defendant must establish a factual basis
and medical rationale for its asserted lack of medical necessity</b>, <span
class=SpellE><u>Nir</u></span><u> v. Allstate Ins. Co.</u>, 7 Misc.3d 544, 796
N.Y.S.2d 857 (Civ. Ct. Kings Co. 2005) (Emphasis Added), which is <b
style='mso-bidi-font-weight:normal'>supported by evidence of the generally
accepted medical/professional practices</b>, <u>A.B. Medical Services, P.L.L.C.
v. <span class=GramE>New<span style='text-decoration:none;text-underline:none'>
</span>York Central Mutual Fire Ins<span style='text-decoration:none;
text-underline:none'>. Co., 7 Misc.3d 1018(A), 801 N.Y.S.2d 229 (<span
class=SpellE>Civ.Ct</span>.</span></span></u> <span class=GramE>Kings Co. 2005)
(Emphasis Added), and that <b style='mso-bidi-font-weight:normal'>the services
rendered were inconsistent with those accepted practices</b>.</span> <u>A.R.
Medical Art<span class=GramE>,.</span> <span class=GramE>P.C. v. State Farm
Mutual Auto.</span> Ins, Co.</u>, 11 Misc.3d 1057 (A), 815 N.Y.S.2d 493 (Civ.
Ct. Kings Co. 2006<span class=GramE>)(</span>Emphasis Added).<o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>8.<span
style='mso-tab-count:1'>   </span>However, <span class=SpellE>conclusory</span>
opinion, standing alone, is insufficient to demonstrate the lack of medical
necessity. <span class=GramE><u>City Wide Social Work &amp; <span class=SpellE>Psy</span>.</u></span><u>
<span class=SpellE>Serv</span>, P.L.L.C. v. Travelers Indemnity Co.,</u> 3
Mise.3d 608, 77 N.Y.S.2d 241 (Civ. Ct. Kings Co. 2004); <u>Ultimate Medical
Supplies v. Lancer Ins. Co.</u>, 7 Mise.3d 1002 (A), 801 N.Y.S.2d 243 (Civ. Ct.
Kings Co. 2004).<o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>9.<span
style='mso-tab-count:1'>   </span><span class=GramE>In</span> the case at bar,
the <b style='mso-bidi-font-weight:normal'>defendant’s expert has failed to
explain how the treatment or services provided were ineffective</b>. Also,
given the patient’s condition, circumstances, and best interests, the <b
style='mso-bidi-font-weight:normal'>expert’s opinion also falls to elaborate
how not providing treatment to the patient would have lead to a better or an
equally good result</b>. The <b style='mso-bidi-font-weight:normal'><u>defendant’s
expert opinion is <span class=SpellE>conclusory</span> and insufficient</u>.</b>
Therefore, defendant’s proof fails to prima facie demonstrate a lack of medical
necessity for the treatment in question.<o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>10.<span
style='mso-tab-count:1'> </span>Moreover, CPLR 3212 (f) states: “Should it
appear from affidavits submitted in opposition to the motion that facts
essential to justify opposition may exist but cannot then be stated, the court
may deny the motion...” In its Cross Motion for Summary Judgment, defendant
argues that the bills at issue in this action were denied based on lack of
medical necessity. In support of its argument, defendant annexes the peer
review report. <b style='mso-bidi-font-weight:normal'><u>However, the report is
not in a proper form and is not sufficient to support a motion</u></b>. <span
class=GramE>(As the burden of coming forward with admissible evidence is
stronger for a <span class=SpellE>movant</span> than a respondent).</span><o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>11.<span
style='mso-tab-count:1'> </span>Further, in the above peer review, the
physician discusses the medical records that were being reviewed along with the
bills. Such documents were not provided by plaintiff and are hearsay.<o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>12.<span
style='mso-tab-count:1'> </span>Plaintiff is not in the possession of all of
the information and documents that were being relied on by the peer-reviewing
physician and has no ability to review or “cross-examine” such documents. <span
class=GramE>Since Plaintiff lacks documents that are “essential to justify
opposition”, defendant’s motion should be denied.</span><o:p></o:p></span></p>

<p class=p36 style='margin-left:0in;text-align:justify;text-indent:.25in;
line-height:150%;tab-stops:0in'><span style='font-size:11.0pt;line-height:150%'>13.<span
style='mso-tab-count:1'> </span>Moreover, in <u>AAA Chiropractic P.C. v.
American Transit Ins. Co.</u>, 238 N.Y<span class=GramE>,L.J</span>. 39 (<span
class=SpellE>Civ</span> Ct Kings 2007), the court, when faced with a
defendant’s cross-motion based on an IME, held,<o:p></o:p></span></p>

<p class=p37 style='text-indent:0in;line-height:150%;tab-stops:0in 21.25pt 54.95pt'><span
style='font-size:8.0pt;line-height:150%'>“Once plaintiff establishes a prima
facie case, which defendant has not disputed, the <b style='mso-bidi-font-weight:
normal'>presentation of evidence of lack of medical necessity creates a
question of fact </b>which, if material, would be a basis to deny summary
judgment to either party. [Citations omitted] Defendant’s presentation of
